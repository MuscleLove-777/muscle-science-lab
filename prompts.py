"""筋トレサイエンスラボ - プロンプト定義

科学的根拠に基づく筋トレ情報ブログ用のプロンプトを一元管理する。
"""


def build_keyword_prompt(config):
    """キーワード選定プロンプトを構築する"""
    categories_text = "\n".join(f"- {cat}" for cat in config.TARGET_CATEGORIES)
    return (
        "筋トレ・トレーニング科学ブログ用のキーワードを選定してください。\n\n"
        "以下のカテゴリから1つ選び、そのカテゴリで今注目されている"
        "筋トレ・運動科学関連のキーワードを1つ提案してください。\n\n"
        f"カテゴリ一覧:\n{categories_text}\n\n"
        "筋トレに興味がある初心者〜中級者が検索しそうなキーワードを意識してください。\n"
        "最新の研究やエビデンスに基づくトピックを優先してください。\n\n"
        "以下の形式でJSON形式のみで回答してください（説明不要）:\n"
        '{"category": "カテゴリ名", "keyword": "キーワード"}'
    )


def build_article_prompt(keyword, category, config):
    """筋トレ科学用の記事生成プロンプトを構築する"""
    return f"""あなたは運動生理学の専門家であり、一般向けのフィットネスサイエンスライターです。

以下のキーワードに関する科学的根拠に基づいた筋トレ情報を、わかりやすい記事にしてください。

【基本条件】
- ブログ名: {config.BLOG_NAME}
- キーワード: {keyword}
- カテゴリ: {category}
- 言語: 日本語
- 文字数: {config.MAX_ARTICLE_LENGTH}文字以内

【記事フォーマット】
## 3行でわかるポイント
- ポイント1
- ポイント2
- ポイント3

## 科学的根拠
（研究論文や実験データに基づく解説。出典を明記）

## 実践トレーニングメニュー
（具体的なセット数・レップ数・休憩時間を含む）

## よくある間違い
（初心者がやりがちなNG行動とその理由）

## まとめ
（明日から実践できるアクションプラン）

【SEO要件】
1. タイトルにキーワード「{keyword}」を必ず含めること
2. タイトルは32文字以内で魅力的に
3. H2、H3の見出し構造を適切に使用すること
4. キーワード密度は{config.MIN_KEYWORD_DENSITY}%〜{config.MAX_KEYWORD_DENSITY}%を目安に
5. メタディスクリプションは{config.META_DESCRIPTION_LENGTH}文字以内

【条件】
- {config.MAX_ARTICLE_LENGTH}文字以内
- 専門用語には必ず簡単な補足説明を付ける
- 具体的な数字やデータを含める（セット数、レップ数、休憩時間など）
- 科学的根拠を示す（〇〇大学の研究では…など）
- 内部リンクのプレースホルダーを2〜3箇所に配置（{{{{internal_link:関連トピック}}}}の形式）

【出力形式】
以下のJSON形式で出力してください。JSONブロック以外のテキストは出力しないでください。

```json
{{
  "title": "SEO最適化されたタイトル",
  "content": "# タイトル\\n\\n本文（Markdown形式）...",
  "meta_description": "120文字以内のメタディスクリプション",
  "tags": ["タグ1", "タグ2", "タグ3", "タグ4", "タグ5"],
  "slug": "url-friendly-slug"
}}
```

【注意事項】
- content内のMarkdownは適切にエスケープしてJSON文字列として有効にすること
- tagsは5個ちょうど生成すること
- slugは半角英数字とハイフンのみ使用すること
- エビデンスベースの情報を心がけ、根拠のない民間療法的な内容は避けること
- 怪我のリスクがある場合は必ず注意喚起を入れること"""
